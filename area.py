
# This function will calculate the area of a square
def square(side):
    area = side * side
    print(f"The area of the square is {area} square units.")


# This function will calculate the area of a circle
def circle(radius):
    area_cir = 3.14 * (radius * radius)
    print(f"The area of the circle is {area_cir} sqaure units.")


# Calls the functions that were defined above
square(4)
circle(5)
